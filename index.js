// Create the add()
function add(a, b) {
    // return 767   // hard coding, never do that!
    return a + b
}


// Create the subtract()s
function subtract(a, b) {
    return a - b
}

// Create the multiply()
function multiply(a, b) {
    return a * b
}


// Create the divide()
function divide(a, b) {
    return a / b;
}


// Create the increment()
function increment(a) {
    return a += 1
}


// Create the decrement()
function decrement(a) {
    return a -= 1;
}


// Create the makeInt()
function makeInt(string) {
    return parseInt(string, 10);
}


// Create the preserveDecimal()
function preserveDecimal(string) {
    return parseFloat(string)
}